package main;

public class Operacoes {
	
	public static void soma(int a, int b) {
		int num1= a, num2=b;
		int res=num1 + num2;
		System.out.println("a soma é: " + res);
	}
	public static void subtracao(int a, int b) {
		int num1= a, num2=b;
		int res=num1 - num2;
		System.out.println("a subtração é: " + res);
	}
	public static void multiplicacao(int a, int b) {
		int num1= a, num2=b;
		int res=num1 * num2;
		System.out.println("a multiplicação é: " + res);
	}
	public static void divisao(int a, int b) {
		int num1= a, num2=b;
		int res=num1 / num2;
		System.out.println("a divisão é: " + res);
	}
}
